/*
    JavaScript Booleans

    Um JavaScript Boolean representa um de dois valores: true ou false
*/

// Função para descobrir se uma expressão ou variável é verdadeira
console.log(Boolean(10 > 9)); // retorna true

// Booleanos de JavaScript como objetos
let x = false;
let y = new Boolean(false);
console.log(x == y); // retorna true
console.log(x === y); // retorna false
