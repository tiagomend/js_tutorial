// Operadores de atribuição em JavaScript

let x = 5;
let y = 10;

// Operador de atribuição simples
console.log(x = y); // retorna 10

// Operador de atribuição de adição
console.log(x += 5); // retorna 15

// Operador de atribuição de subtração
console.log(x -= 5); // retorna 10

// Operador de atribuição de multiplicação
console.log(x *= 2); // retorna 20

// Operador de atribuição de exponenciação
console.log(x **= 2); // retorna 400

// Operador de atribuição de divisão
console.log(x /= 40); // retorna 10

// Operador de atribuição de resto de divisão
console.log(x %= 3); // retorna 1

// Operador de atribuição de deslocamento à esquerda
console.log(x <<= 5); // retorna 32

// Operador de atribuição de deslocamento à direita
console.log(x >>= 5); // retorna 1

